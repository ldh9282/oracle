-- 2022-11-01 11:43:30 +9:00

-- TIMESTAMP VS TO_TIMESTAMP_TZ
SELECT TIMESTAMP '2022-11-01 11:43:30 ASIA/SEOUL'
       AT TIME ZONE 'UTC'
FROM DUAL;

SELECT TO_TIMESTAMP_TZ('2022.11.01 11:43:30 ASIA/SEOUL',
                       'YYYY.MM.DD HH24:MI:SSXFF TZR')  
       AT TIME ZONE 'UTC'
FROM DUAL;


-- TIMESTAMP VS TO_TIMESTAMP_TZ
-- AND CHANGE THE TIME ZONE 

SELECT TO_CHAR(
           TIMESTAMP '2022-11-01 11:43:30 ASIA/SEOUL'  AT TIME ZONE 'UTC',
           'yy/MM/dd (DAY) AM HH24:MI:SS (TZR)')
FROM DUAL;


SELECT TO_CHAR(
           TO_TIMESTAMP_TZ('2022.11.01 11:43:30 ASIA/SEOUL',
                           'YYYY.MM.DD HH24:MI:SSXFF TZR')  AT TIME ZONE 'UTC',
           'yy/MM/dd (DAY) AM HH24:MI:SS (TZR)')
FROM DUAL;

SELECT TO_CHAR( 
        TO_TIMESTAMP_TZ('17/08/08 2:00:00 +09:00', 'YY/MM/DD HH24:MI:SS TZR'),
        'AM HH24:MI:SS ("UTC"TZR) YYYY.MM.DD DAY')
FROM DUAL;